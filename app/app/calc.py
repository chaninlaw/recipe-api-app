"""
Calculator functions
"""


def add(x: int, y: int):
    """Add two numbers together."""
    if (x is not int) or (y is not int):
        return 0
    return x + y


def subtract(x: int, y: int,):
    """Subtract two numbers together."""
    if (x is not int) or (y is not int):
        return 0
    return x - y
